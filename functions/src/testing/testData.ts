export const getRandomItem = <T>(array: T[]): T => {
  return array[Math.floor(Math.random() * array.length)];
};

export const getRandomItems = <T>(array: T[], count: number): T[] => {
  const shuffled = [...array].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, array.length));
};

export const getRandomInt = (min: number, max: number): number => {
  return Math.floor(Math.random() * (max - min + 1)) + min;
};

export const getRandomDate = (start: Date, end: Date): Date => {
  return new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()));
};

export const generateId = (prefix: string = "test_"): string => {
  return `${prefix}${Date.now().toString(36)}${Math.random().toString(36).substring(2, 9)}`;
};

export const getRandomBoolean = (trueWeight: number = 0.5): boolean => {
  return Math.random() < trueWeight;
};

export const generateUsername = (firstName: string, lastName: string): string => {
  const suffix = getRandomInt(1, 999);
  return `${firstName.toLowerCase()}${lastName.toLowerCase()}${suffix}`;
};

export const getISODate = (date: Date = new Date()): string => {
  return date.toISOString();
};

export const getFutureDate = (daysFromNow: number): Date => {
  const date = new Date();
  date.setDate(date.getDate() + daysFromNow);
  return date;
};

export const getPastDate = (daysAgo: number): Date => {
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);
  return date;
};

